FOR 문

FOR문은 가장 기본적인 반복문입니다. 특정 값에 변화를 주어가면서 우리가 정한 조건이 만족 될 때까지 계속 반복합니다.
그러면 실습을 통해서 알아보겠습니다
''';

var code1 = '''
for i in range(10) :
    print(i)
''';

var ex02 = '''
실습을 통해서 볼 수 있듯이 10번을 반복하면서 i의 값이 1씩 늘어나는 것을 볼 수 있습니다.
사실 range(초기값,조건,증가값)으로 되어 있습니다. 하지만 여기서 초기값과 증가값을 안줘도 되는데 이때, 초기값은 0으로 설정되고 증가값은 1이 됩니다.
따라서 특정 횟수만큼 실행해야 할 때는 range()를 많이 씁니다.
그러면 특정 횟수가 지정되지 않았을 때는 어떻게 써야할까요? 실습을 통해서 알아보겠습니다.
''';

