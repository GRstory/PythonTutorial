list = ['One','Two','Three']
for i in list:
    print(i)
''';

var ex03 = '''
실습에서 보았듯이 list의 길이만큼 실행하고 싶을 때에는 range() 대신에 list의 변수명을 많이 써줍니다.
반복문 안에 반복문을 써서 다중 반복을 시킬 수도 있습니다. 이것은 퀴즈에서 직접 고민해서 해보시길 바랍니다:)


WHILE 문

WHILE문도 반복문 중 하나인데 FOR문보다 더 직관적인 문법입니다.
왜 더 직관적인지 실습을 통해 알아볼까요?
''';

var code3 = '''
i = 0
while(i<10) : 
    print(i)
    i+=1
''';

var ex04 = '''
실습을 통해서 알 수 있듯이 i의 값이 10보다 작거나 같을 때까지 반복하면서 WHILE문 안의 코드를 실행하는 것을 보실 수 있습니다.
WHILE문은 while(조건) 이런식으로 쓰이기 때문에, FOR문의 range()과는 다르게 초기값과 증감값을 직접 설정해 주어야 합니다. 그래서 보통 WHILE문에 들어가기 전에 변수를 선언하고, WHILE문 도중에 변수값을 증감 시켜 줍니다.
